/**
 * Dynamic Syllabus — Project-aware task generation
 *
 * TASK 20/22: CTO Brain Upgrade
 *
 * Reads project profiles + learning insights to generate
 * targeted improvement tasks. Replaces dumb round-robin
 * with intelligent "teach to the weakness" approach.
 */

const fs = require('fs');
const path = require('path');
const config = require('../config');

function log(msg) {
	const ts = new Date().toISOString().slice(11, 19);
	const line = `[${ts}] [syllabus] ${msg}\n`;
	try {
		fs.appendFileSync(config.LOG_FILE, line);
	} catch (_) {}
}

/**
 * Generate focused tasks based on project profile weaknesses
 * @param {object} profile - From project-profiler.analyzeProject()
 * @returns {Array<{ priority: string, task: string, rationale: string }>}
 */
function generateTasksFromProfile(profile) {
	const tasks = [];

	if (!profile || !profile.metrics) return tasks;

	// TypeScript errors → fix task
	if (profile.metrics.tsErrors > 0) {
		tasks.push({
			priority: profile.metrics.tsErrors > 50 ? 'HIGH' : 'MEDIUM',
			task: `Fix all TypeScript errors in ${profile.project}. Run \`npx tsc --noEmit\` and fix each error.`,
			rationale: `${profile.metrics.tsErrors} TS errors reduce code quality`,
		});
	}

	// Lint errors → fix task
	if (profile.metrics.lintErrors > 0) {
		tasks.push({
			priority: profile.metrics.lintErrors > 20 ? 'HIGH' : 'MEDIUM',
			task: `Fix all lint errors in ${profile.project}. Run \`npm run lint -- --fix\` then fix remaining.`,
			rationale: `${profile.metrics.lintErrors} lint errors`,
		});
	}

	// No tests → add tests
	if (!profile.metrics.hasTests) {
		tasks.push({
			priority: 'HIGH',
			task: `Add unit tests for core modules in ${profile.project}. Focus on the most critical business logic files.`,
			rationale: 'No test files found — critical quality gap',
		});
	} else if (profile.metrics.testFileCount < 3) {
		tasks.push({
			priority: 'MEDIUM',
			task: `Add more unit tests for ${profile.project}. Currently only ${profile.metrics.testFileCount} test files — increase coverage.`,
			rationale: 'Low test coverage',
		});
	}

	// Stale project → review
	if (profile.metrics.lastCommitDaysAgo > 60) {
		tasks.push({
			priority: 'LOW',
			task: `Review ${profile.project} for relevance. Last commit was ${profile.metrics.lastCommitDaysAgo} days ago. Archive if no longer needed.`,
			rationale: 'Project may be abandoned',
		});
	}

	// Outdated deps → update
	if (profile.metrics.outdatedDeps > 10) {
		tasks.push({
			priority: 'MEDIUM',
			task: `Update outdated dependencies in ${profile.project}. Run \`npm outdated\` and update critical packages.`,
			rationale: `${profile.metrics.outdatedDeps} outdated deps — potential security issues`,
		});
	}

	return tasks;
}

/**
 * Generate tasks for all projects, sorted by priority
 * @param {Array<object>} profiles - From project-profiler.analyzeAllProjects()
 * @returns {Array<object>} Sorted task list
 */
function generateSyllabus(profiles) {
	const allTasks = [];

	for (const profile of profiles) {
		const tasks = generateTasksFromProfile(profile);
		allTasks.push(...tasks.map((t) => ({ ...t, project: profile.project, healthScore: profile.healthScore })));
	}

	// Sort: CRITICAL > HIGH > MEDIUM > LOW, then by health score (lowest first)
	const priorityOrder = { CRITICAL: 0, HIGH: 1, MEDIUM: 2, LOW: 3 };
	allTasks.sort((a, b) => {
		const pa = priorityOrder[a.priority] ?? 4;
		const pb = priorityOrder[b.priority] ?? 4;
		if (pa !== pb) return pa - pb;
		return (a.healthScore || 0) - (b.healthScore || 0);
	});

	return allTasks;
}

/**
 * Write task files to WATCH_DIR for CTO to execute
 * @param {Array<object>} tasks
 * @param {number} [limit=5] - Max tasks to write
 */
function writeSyllabusToTasks(tasks, limit = 5) {
	const watchDir = config.WATCH_DIR;
	if (!watchDir || !fs.existsSync(watchDir)) return;

	const written = [];
	for (const task of tasks.slice(0, limit)) {
		const filename = `${task.priority}_mission_${task.project}_auto_syllabus_${Date.now()}.txt`;
		const content = `${task.task}\n\n# Rationale: ${task.rationale}\n# Project: ${task.project}\n# Health Score: ${task.healthScore || '?'}/100\n# Generated by Dynamic Syllabus v1`;

		try {
			fs.writeFileSync(path.join(watchDir, filename), content);
			written.push(filename);
			log(`📋 Syllabus task: ${filename}`);
		} catch (e) {
			log(`Failed to write task: ${e.message}`);
		}
	}

	return written;
}

module.exports = { generateTasksFromProfile, generateSyllabus, writeSyllabusToTasks };
