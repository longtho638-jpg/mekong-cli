/**
 * Generate complete handover package for a project.
 * @param {string} projectDir - Absolute path to project
 * @param {string} projectName - Human-readable project name
 * @param {object} scoreResult - Output from calculateProjectScore()
 * @returns {object} { outputDir, files[] }
 */
function generateHandoverPackage(projectDir, projectName, scoreResult) {
	const outputDir = path.join(projectDir, 'docs', 'handover');

	// Ensure output directory exists
	fs.mkdirSync(outputDir, { recursive: true });

	const files = [
		{ name: 'DELIVERY_REPORT.md', content: generateDeliveryReport(projectName, scoreResult) },
		{ name: 'ACCEPTANCE_CERTIFICATE.md', content: generateAcceptanceCertificate(projectName, scoreResult) },
		{ name: 'USER_GUIDE.md', content: generateUserGuide(projectName) },
		{ name: 'CREDENTIALS_CHECKLIST.md', content: generateCredentialsChecklist(projectName) },
		{ name: 'MAINTENANCE_OPTIONS.md', content: generateMaintenanceOptions(projectName) },
	];

	const written = [];
	for (const file of files) {
		const filePath = path.join(outputDir, file.name);
		fs.writeFileSync(filePath, file.content, 'utf-8');
		written.push(filePath);
	}

	return { outputDir, files: written };
}

module.exports = { generateHandoverPackage };
