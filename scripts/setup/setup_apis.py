BLUE = "\033[94m"
RESET = "\033[0m"


def print_header(title):
    print(f"\n{BOLD}{BLUE}{'=' * 60}{RESET}")
    print(f"{BOLD}{BLUE}{title}{RESET}")
    print(f"{BOLD}{BLUE}{'=' * 60}{RESET}\n")


def print_step(n, title, url):
    print(f"\n{YELLOW}Step {n}: {title}{RESET}")
